              <p class="fontP">Alibaba Cloud หนึ่งในผู้ให้บริการ IaaS ชั้นนำของโลกและอันดับ 1 ของเอเชียแปซิฟิกสำหรับ IaaS และ IUS (Infrastructure Utility Services) ที่มีส่วนแบ่งการตลาดติดต่อกันเป็นเวลา 2 ปีซ้อน และตามข้อมูลของ IDC เป็นผู้ให้บริการ Public cloud ที่ใหญ่ที่สุดในประเทศจีน และ ได้ลงนามในข้อตกลงร่วมกับอินเทอร์เน็ตประเทศไทย (INET) โดยแต่งตั้งให้เป็นผู้ให้บริการ Alibaba Cloud Computing รายแรกในประเทศไทย</p> 
